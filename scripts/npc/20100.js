var status = 0;

function start() {
	status = -1;
	action(1, 0, 0);
}

function action(mode, type, selection) {
	if (mode == -1) {
		cm.dispose();
	} else {
		if (mode == 1)
			status++;
		else
			status--;
		if (status == 0) {
			cm.sendNext("这里是彩虹村的南边，在这里可以坐船出海哦！");
		} else if (status == 1) {
			cm.sendOk("想体验的话，赶紧走咯！");
			cm.dispose();
		}
	}
}	
