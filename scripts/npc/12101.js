var status = 0;

function start() {
	status = -1;
	action(1, 0, 0);
}

function action(mode, type, selection) {
	if (mode == -1) {
		cm.dispose();
	} else {
		if (mode == 1)
			status++;
		else
			status--;
		if (status == 0) {
			cm.sendNext("我是瑞恩，最近村里来了好多小伙伴，顿时有点不适应了！传送点也由于压力过大，出现了一些问题，但应该还能工作，就先这样吧！");
		} else if (status == 1) {
			cm.sendOk("要不我们加个微信吧，来，扫一扫......");
			cm.dispose();
		}
	}
}	
